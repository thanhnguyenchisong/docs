const fs = require('fs');

function fillTranslations(filePath, translations) {
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  let filled = 0;
  for (const [key, value] of Object.entries(translations)) {
    if (data.hasOwnProperty(key) && (!data[key] || data[key].length === 0)) {
      data[key] = value;
      filled++;
    }
  }
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
  
  // Count remaining
  const remaining = Object.entries(data).filter(([k, v]) => !v || v.length === 0).length;
  console.log(`${filePath}: filled ${filled} entries, ${remaining} still empty`);
}

